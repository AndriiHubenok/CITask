package org.example;

public class Calculator {
    public Long add(Long a, Long b){
        return a + b;
    }

    public Long subtract(Long a, Long b){
        return a - b;
    }

    public Long multiply(Long a, Long b){
        return a * b;
    }

    public double divide(Long a, Long b){
        return (double) a / b;
    }
}
